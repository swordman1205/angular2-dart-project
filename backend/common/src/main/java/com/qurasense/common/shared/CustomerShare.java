package com.qurasense.common.shared;

public class CustomerShare {

    private String email;
    private String fullName;
    private String phone;
    private String addressLine;
    private String city;
    private String zip;
    private String state;
    private String callIntervals;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddressLine() {
        return addressLine;
    }

    public void setAddressLine(String addressLine) {
        this.addressLine = addressLine;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCallIntervals() {
        return callIntervals;
    }

    public void setCallIntervals(String callIntervals) {
        this.callIntervals = callIntervals;
    }
}
